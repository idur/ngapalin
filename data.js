// Fungsi helper untuk mendapatkan data surat
function getSurahData(surahNumber) {
    return juz30Data[surahNumber] || null;
}

// Fungsi helper untuk mendapatkan data ayat
function getAyahData(surahNumber, ayahNumber) {
    const surah = getSurahData(surahNumber);
    if (!surah || !surah.ayahs[ayahNumber]) {
        return null;
    }
    return surah.ayahs[ayahNumber];
}

// Fungsi untuk mendapatkan daftar surat dalam Juz 30
function getSurahList() {
    return Object.keys(juz30Data).map(surahNumber => ({
        number: parseInt(surahNumber),
        name: juz30Data[surahNumber].name,
        nameArabic: juz30Data[surahNumber].nameArabic,
        totalAyahs: juz30Data[surahNumber].totalAyahs
    }));
}

// Fungsi untuk mendapatkan daftar ayat dalam surat
function getAyahList(surahNumber) {
    const surah = getSurahData(surahNumber);
    if (!surah) return [];
    
    return Object.keys(surah.ayahs).map(ayahNumber => ({
        number: parseInt(ayahNumber),
        arabic: surah.ayahs[ayahNumber].arabic,
        translation: surah.ayahs[ayahNumber].translation
    }));
}

